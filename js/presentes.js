data.presentes.sort((a, b) => {
    // Remover o caractere ',' e converter para número
    const precoA = parseFloat(a.preco.replace('.', '').replace(',', '.'));
    const precoB = parseFloat(b.preco.replace('.', '').replace(',', '.'));
    return precoA - precoB; // Ordenação crescente
});

// Função para criar as LIs dinamicamente
data.presentes.forEach(presente => {
    // Cria o elemento <li>
    const li = document.createElement("li");
    li.className = "one-third animate-box";
    li.setAttribute("data-animate-effect", "fadeIn");
    li.style.backgroundImage = `url(${presente.imagem})`;

    const icon = presente.link !== ''
        ? '<span class="icon" style="color:white;font-size:30px;"><i class="fa-solid fa-arrow-up-right-from-square"></i></span>'
        : '<span class="icon" style="color:white;font-size:30px;"><i class="fa-brands fa-pix"></i></span>';

    const link = presente.link !== ''
        ? `<a href="${presente.link}" target="_blank" class="${presente.recebido ? "recebido" : ""}">`
        : `<a href="./images/yj/qrcode.png" data-fancybox data-caption="Clique fora para voltar" class="${presente.recebido ? "recebido" : ""}">`;

    const fita = presente.recebido
        ? '<div class="case-studies-fita"><img style="width: -webkit-fill-available;" src="./images/wj/fita.png" alt=""><p style="text-align: center;margin-top: -25px;color: white;">😱 Brigaduu! ❤️</p></div>'
        : '';
        

    // Conteúdo interno do <li>
    li.innerHTML = `
    ${link}
      ${fita}
      <div class="case-studies-summary">
        <span>R$ ${presente.preco}</span>
        <h2>${presente.descricao}</h2>
      </div>
      <div class="case-studies-icon">
        ${icon}
      </div>
    </a>
  `;

    // Adiciona o <li> ao <ul>
    galleryList.appendChild(li);
});
